package com.yang.oa.commons.exception;

public class JlException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7239063277585454932L;

	public JlException() {

		super();

	}

	public JlException(String msg) {

		super(msg);

	}

	public JlException(String msg, Throwable cause) {

		super(msg, cause);

	}

	public JlException(Throwable cause) {

		super(cause);

	}

}
